# Beginner Exercises for Phase 1

## 1. Print Your Name
# Write a program that prints your name.

name = "Your Name"
print(name)

## 2. Simple Mathematical Operations
# Write a program that performs addition, subtraction, multiplication, and division.

num1 = 10
num2 = 5
print(f"Addition: {num1 + num2}")
print(f"Subtraction: {num1 - num2}")
print(f"Multiplication: {num1 * num2}")
print(f"Division: {num1 / num2}")

## 3. List Operations
# Create a list, add an item to it, remove an item, and print the list.

my_list = [1, 2, 3]
my_list.append(4)
my_list.remove(2)
print(my_list)

## 4. Loop through a Range
# Write a program that loops through numbers 0 to 5 and prints each number.

for i in range(6):
    print(i)

## 5. Conditional Statements
# Write a program that checks if a number is even or odd.

number = 4
if number % 2 == 0:
    print("Even")
else:
    print("Odd")
